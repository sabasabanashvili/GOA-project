# ანონიმური ფუნქცია (lambda), რომელიც სტრინგს აბრუნებს შემობრუნებულ და პირველი ასო დიდით
reverse_and_capitalize = lambda s: s[::-1].capitalize()

# ფუნქციის 3-ჯერ გამოძახება სხვადასხვა არგუმენტით
print(reverse_and_capitalize("hello"))      # Olleh
print(reverse_and_capitalize("JavaScript")) # TpircsaVaj
print(reverse_and_capitalize("ChatGPT"))    # Tpgtahc
