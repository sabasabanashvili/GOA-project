# შემოიტანეთ სამი რიცხვი
num1 = int(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = int(input("შეიყვანეთ მეორე რიცხვი: "))
num3 = int(input("შეიყვანეთ მესამე რიცხვი: "))

#შედეგი
print("1:", num1 + num2 + num3) 
print("2:", num1 - num2 - num3)  
print("4:", num1 / num2 / num3)       
print("3:", num1 * num2 * num3)